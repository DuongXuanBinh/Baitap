package Session4;

public class Vidu7 {
    public static void main(String[] args) {
        String day = "Monday";
        switch (day){
            case "Sunday":
                System.out.println("First day of the weeks");
                break;
            case "Monday":
                System.out.println("Second day of the weeks");
                break;
            case "Tuesday":
                System.out.println("Third day of the weeks");
                break;
            default:
                System.out.println("Invalid");
        }
    }
}
